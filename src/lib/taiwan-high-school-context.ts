function matchingLabContexts(subject: string): Array<keyof typeof LAB_CONTEXTS> {
  const normalized = subject.trim().replace(/\s+/g, "");
  if (normalized.includes("物理")) return ["physics"];
  if (normalized.includes("化學")) return ["chemistry"];
  if (normalized.includes("生物")) return ["biology"];
  if (normalized.includes("地球科學") || normalized.includes("地科")) {
    return ["earthScience"];
  }
  if (normalized.includes("自然")) {
    return ["physics", "chemistry", "biology", "earthScience"];
  }
  return [];
}

